package cn.wormholestack.mse.common.constant;

/**
 * @description： 常用字符常量
 * @Author MRyan
 * @Date 2021/11/13 14:25
 * @Version 1.0
 */
public interface Symbol {

    /**
     * 反斜杠
     */
    String BSLASH = "/";

    /**
     * 竖杠
     */
    String VERTICAL = "|";

    /**
     * 竖线拆分符  '|'
     */
    String VERTICAL_SPLIT = "\\|";

    /**
     * 逗号   ','
     */
    String COMMA = ",";

    /**
     * 分号 ';'
     */
    String SEMICOLON = ";";

    /**
     * and符 '&'
     */
    String AMPERSAND = "&";

    /**
     * 星号符 '*'
     */
    String ASTERISK = "\\*";

    /**
     * 短横线
     */
    String STRIGULA = "-";

    /**
     * 插入符号 '^'
     */
    String CARET = "^";

    /**
     * 冒号
     */
    String COLON = ":";

    /**
     * 下划线
     */
    String UNDERLINE = "_";

    /**
     * 中文冒号
     */
    String ZH_COLON = "：";

    /**
     * 问号
     */
    String QUESTION = "?";

    /**
     * 等号
     */
    String EQUAL = "=";

    /**
     * 左方括号
     */
    String OPEN_BRACKET = "[";

    /**
     * 右方括号
     */
    String CLOSE_BRACKET = "]";

    /**
     * 点号
     */
    String DOT = ".";

    /**
     * 波浪字符
     */
    String TILDE = "~";

    /**
     * 点号
     */
    String DOT_SPLIT = "\\.";

    /**
     * @符
     */
    String ATSYMBOL = "@";

    String BLANK = " ";
}
